class SiteInfo {
  final String address;
  final String imageUrl;
  final String management;
  final String name;
  final bool status;
  final double target;
  final String id;

  const SiteInfo({
    required this.address,
    required this.imageUrl,
    required this.management,
    required this.name,
    required this.status,
    required this.target,
    required this.id,
  });
}
